import { writeFile } from "node:fs/promises";

const TALENTS_URL = "https://www.nijisanji.jp/talents";
const TARGET_AFFILIATIONS = new Set(["にじさんじ", "NIJISANJI EN"]);
const SOURCE_LABEL = "NIJISANJI official talents page";

const html = await fetchText(TALENTS_URL);
const nextData = extractNextData(html);
const allLivers = nextData.props.pageProps.allLivers;
const targetLivers = allLivers.filter((liver) =>
  liver.profile?.affiliation?.some((affiliation) => TARGET_AFFILIATIONS.has(affiliation))
);

const livers = [];

for (let index = 0; index < targetLivers.length; index += 1) {
  const listItem = targetLivers[index];
  const detailUrl = `https://www.nijisanji.jp/talents/l/${listItem.slug}`;
  const detail = await fetchLiverDetail(detailUrl);
  const official = detail ?? listItem;
  livers.push(toAppLiver(official, detailUrl));

  process.stdout.write(`Fetched ${index + 1}/${targetLivers.length}: ${listItem.name}\n`);
}

livers.sort((a, b) => (a.sortOrderRuby ?? 9999) - (b.sortOrderRuby ?? 9999));

const generated = `import type { Liver } from "../types";

export const specialDayMilestones = [2434];

export const dataSource = {
  label: ${JSON.stringify(SOURCE_LABEL)},
  url: ${JSON.stringify(TALENTS_URL)},
  fetchedAt: ${JSON.stringify(new Date().toISOString())},
  note: "Generated by scripts/fetch-official-livers.mjs. debutAt is converted to an Asia/Tokyo calendar date."
};

export const livers: Liver[] = ${JSON.stringify(livers, null, 2)};
`;

await writeFile(new URL("../src/data/livers.ts", import.meta.url), generated);

console.log(`Wrote ${livers.length} livers to src/data/livers.ts`);

async function fetchLiverDetail(url) {
  try {
    const html = await fetchText(url);
    const data = extractNextData(html);
    return data.props.pageProps.liverDetail;
  } catch (error) {
    console.warn(`Failed to fetch detail ${url}: ${error.message}`);
    return null;
  }
}

async function fetchText(url) {
  const response = await fetch(url, {
    headers: {
      "user-agent": "NijiDaysDataImporter/0.1 (+local development)"
    }
  });

  if (!response.ok) {
    throw new Error(`${response.status} ${response.statusText}`);
  }

  return response.text();
}

function extractNextData(html) {
  const marker = '<script id="__NEXT_DATA__" type="application/json">';
  const start = html.indexOf(marker);
  if (start < 0) {
    throw new Error("__NEXT_DATA__ marker not found");
  }

  const jsonStart = start + marker.length;
  const jsonEnd = html.indexOf("</script>", jsonStart);
  if (jsonEnd < 0) {
    throw new Error("__NEXT_DATA__ end marker not found");
  }

  return JSON.parse(html.slice(jsonStart, jsonEnd));
}

function toAppLiver(liver, officialUrl) {
  const affiliation = liver.profile?.affiliation?.[0] ?? "にじさんじ";
  const branch = affiliation === "NIJISANJI EN" ? "EN" : "JP";
  const color = liver.profile?.color || fallbackColor(liver.slug);

  return {
    id: liver.slug,
    displayName: liver.name,
    displayNameKana: liver.ruby || liver.name,
    sortOrderRuby: liver.orderByRuby,
    sortOrderEnName: liver.orderByEnName,
    debutDate: toJstDate(liver.profile?.debutAt),
    debutDateBasis: "公式プロフィールのデビュー日",
    branch,
    status: "listed",
    groupName: affiliation,
    generationName: normalizeUnit(liver.unit),
    color,
    sourceNote: `公式タレント詳細ページを参照: ${officialUrl}`,
    links: buildLinks(liver.socialLinks, officialUrl)
  };
}

function buildLinks(socialLinks = {}, officialUrl) {
  const links = [{ type: "official", label: "公式プロフィール", url: officialUrl }];
  const mapping = [
    ["twitter", "x", "X"],
    ["youtube", "youtube", "YouTube"],
    ["fanclub", "fanclub", "ファンクラブ"],
    ["officialShop", "officialShop", "公式ショップ"],
    ["officialStoreEn", "officialStoreEn", "Official Store EN"],
    ["magazine", "magazine", "MAGAZINE"]
  ];

  for (const [sourceKey, type, label] of mapping) {
    const url = socialLinks[sourceKey];
    if (url) {
      links.push({ type, label, url });
    }
  }

  return links;
}

function normalizeUnit(unit) {
  if (!unit || unit === false) {
    return undefined;
  }

  if (typeof unit === "string") {
    return unit;
  }

  if (Array.isArray(unit)) {
    return unit.map((item) => item.name || item.title || item).filter(Boolean).join(" / ") || undefined;
  }

  return unit.name || unit.title || undefined;
}

function toJstDate(value) {
  if (!value) {
    throw new Error("Missing debutAt");
  }

  const date = new Date(value);
  const parts = new Intl.DateTimeFormat("en-CA", {
    timeZone: "Asia/Tokyo",
    year: "numeric",
    month: "2-digit",
    day: "2-digit"
  }).formatToParts(date);

  const year = parts.find((part) => part.type === "year").value;
  const month = parts.find((part) => part.type === "month").value;
  const day = parts.find((part) => part.type === "day").value;

  return `${year}-${month}-${day}`;
}

function fallbackColor(seed) {
  let hash = 0;
  for (const char of seed) {
    hash = (hash * 31 + char.charCodeAt(0)) % 360;
  }

  return `hsl(${hash} 54% 48%)`;
}
